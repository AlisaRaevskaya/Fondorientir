     title="Бесплатная консультация">
 <p><u>Основные данные, которые иностранцу необходимо внести в бланк уведомления:</u></p>
 <ul>
     <li>Свою фамилию имя и отчество;</li>
     <li>Адрес места проживания;</li>
     <li>Место и длительность работы. Если за год иностранец сменил несколько мест работы, то он должен в
         уведомлении
         указать их все;</li>
     <li>Период (периоды) нахождения за пределами России с указанием всех государств, куда иностранец
         уезжал
         за
         прошедший
         год (При необходимости).</li>
 </ul>
 <p>Важно помнить! Вместе с уведомлением иностранный гражданин должен предоставить документ о доходах за
     прошедший
     год.
     Подать их необходимо только в то ГУВМ МВД (бывший ФМС), где РВП было выдано.</p>
 <p><u>В качестве документа о доходах иностранный гражданин может предоставить справку:</u></p>
 <p>- о доходах;</p>
 <p>- о получении алиментов;</p>
 <p>- о доходах лица, на иждивении у которого официально находится иностранец;</p>
 <p>- о наличии банковского вклада. Размер вклада напрямую зависит от установленного правительством РФ
     размера
     прожиточного минимума в регионе, где было получено РВП;</p>
 <p>- Иные документы о получение официального дохода от не запрещенной законом деятельности</p>
 <p>&nbsp;</p>
 <p>Подтвердить свое проживание по РВП вы можете самостоятельно. Для этого вам нужно лично обратиться в
     то
     отделение
     ГУВМ
     МВД , где РВП было вам выдано.</p>
