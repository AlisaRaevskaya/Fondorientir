 <strong><u>Подтверждение РВП</u></strong>

 <p>Пункт 9 ст. 6 ФЗ 115 гласит, что те иностранные граждане, которые в России получили для себя
     разрешение
     на
     временное
     проживание, должны ежегодно подавать в ГУВМ МВД подтверждение своего проживания по РВП в
     Санкт-Петербурге или
     Ленинградской области.</p>
 <p>Очень часто о данном требовании федерального закона иностранцы не знают или просто забывают.&nbsp;А
     ведь
     если
     соответствующее уведомление в ГУВМ МВД (бывший ФМС)&nbsp;не отправить или сделать это нарушив сроки,
     установленные
     законом РФ, то РВП иностранного гражданина будет аннулировано.</p>
 <p>Чтобы этого избежать необходимо каждый год подавать в ГУВМ МВД уведомление о подтверждении
     проживания,
     установленной
     законом формы ( Бланк № 4), вместе с документом, в котором будет указан источник и размер дохода за
     прошедший
     год.
 </p>
