<ul>
    <li>Вид на жительство (ВНЖ) в РФ</li>
</ul>
<p><strong>Вид на жительство</strong> (<strong>ВНЖ</strong>) &ndash; это документ, подтверждающий право
    иностранного
    гражданина на постоянное проживание и свободное перемещение по стране. &nbsp;</p>
<p>Вид на жительство выдается бессрочно (ранее выдавался на 5 лет и по его окончанию мог быть продлен
    еще на 5 лет).
    За
    исключением отдельных случаев: например, вида на жительство для высококвалифицированного сотрудника,
    которое
    выдается на срок действия его разрешения на работу.</p>
<p>Работать с видом на жительство можно без патента и разрешения на работу в любом регионе РФ,
    независимо от
    того,
    где
    ВНЖ выдан.</p>
