 <p><u>&nbsp;</u></p>
 <p><strong>Гражданство РФ </strong>(<strong>Гражданство</strong>) &ndash; это правовой статус, который
     дает
     человеку
     определенные права в государстве, чьим гражданином он является.</p>
 <p>Перед тем как получить гражданство, у иностранного гражданина должен быть оформлен вид на жительство.
 </p>
 <p><strong>Иностранный гражданин может получить гражданство РФ в нескольких случаях:</strong></p>
 <p>1.В общем порядке;</p>
 <p>2.В упрощенном порядке;</p>
 <p>3.Вступив в государственную программу по переселению соотечественников.</p>
 <p><strong>Какие существуют основания для получения гражданства РФ в общем порядке?</strong></p>
 <ul>
     <li>Рождение на территории РФ;</li>
     <li>Прием в гражданство Российской Федерации;</li>
     <li>Восстановление в гражданстве Российской Федерации;</li>
     <li>Иные основания, предусмотренные ФЗ №62 от 31.05.2002г. или иным международным договором.</li>
 </ul>
